# Example of variables and data types in Python

# Numeric variables
integer = 42  # Integer (int): represents a whole number
floating_number = 3.14  # Float: represents a decimal number

# Strings
string = "Hello World"  # String: represents a sequence of characters
multiline_string = """This is
a multiline
string"""  # Multiline string: represents a string that spans multiple lines

# Booleans
variable_true = True   # Boolean (bool): can be True or False
variable_false = False # Boolean (bool): can be True or False

# Lists
list = [1, 2, 3, 4, 5]  # List: an ordered collection of elements
mixed_list = [1, "Hello", 3.14, True]  # List with different data types

# Tuples
tuple = (1, 2, 3, 4, 5)  # Tuple: similar to a list, but immutable

# Sets
set_variable = {1, 2, 3, 4, 5}  # Set: a collection of unique elements without order

# Dictionaries
dictionary = {
    "name": "Alice",
    "age": 30,
    "is_student": True
}  # Dictionary: a collection of key-value pairs

# None
none_variable = None  # None: represents the absence of a value

# Print variable values
print(integer)        # Prints the value of the integer
print(floating_number)          # Prints the value of the float
print(string)         # Prints the string
print(multiline_string)        # Prints the multiline string
print(variable_true)   # Prints the boolean value True
print(variable_false)  # Prints the boolean value False
print(list)           # Prints the list
print(mixed_list)              # Prints the list with different data types
print(tuple)          # Prints the tuple
print(set_variable)            # Prints the set
print(dictionary)     # Prints the dictionary
print(none_variable)           # Prints None